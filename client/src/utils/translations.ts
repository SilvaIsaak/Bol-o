export const translateTeam = (name: string) => {
  if (!name) return '';
  const trimmedName = name.trim();
  
  // If we have a translation for it (EN -> PT), return it
  if (teamTranslations[trimmedName]) return teamTranslations[trimmedName];
  
  // If the name is already a value in our translations (PT), return it
  const isAlreadyTranslated = Object.values(teamTranslations).includes(trimmedName);
  if (isAlreadyTranslated) return trimmedName;

  return trimmedName;
};

export const translateStage = (stage: string) => {
  if (!stage) return '';
  const trimmedStage = stage.trim();
  
  // Handle some common API variations
  const upperStage = trimmedStage.toUpperCase();
  if (stageTranslations[upperStage]) return stageTranslations[upperStage];
  
  return stageTranslations[trimmedStage] || trimmedStage;
};

export const getFlagUrl = (teamName: string) => {
  if (!teamName) return 'https://flagcdn.com/w80/un.png';
  
  const normalizedInput = teamName.trim().toLowerCase();
  
  // 1. Try direct mapping (lowercase)
  if (flagCodes[normalizedInput]) {
    return `https://flagcdn.com/w80/${flagCodes[normalizedInput].toLowerCase()}.png`;
  }

  // 2. Try to find the English name if input is Portuguese
  const englishName = Object.keys(teamTranslations).find(
    key => teamTranslations[key].toLowerCase() === normalizedInput
  );
  
  if (englishName && flagCodes[englishName.toLowerCase()]) {
    return `https://flagcdn.com/w80/${flagCodes[englishName.toLowerCase()].toLowerCase()}.png`;
  }

  return 'https://flagcdn.com/w80/un.png';
};


